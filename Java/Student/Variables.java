//变量
class Variables
{
    public static void main(String[] args)
    {
        boolean a = true; // 布尔型变量
        byte b = 127;// 字节型变量
        short c = 32767; // 短整型变量
        int d = 2147483647; // 整型变量
        long e = 10000000000L; // 长整型变量

        float f = 20.5f; // 浮点型变量
        double g = 30.5d; // 双精度型变量

        char h = 'A'; // 字符型变量
        String i = "Hello world!"; // 字符串型变量
        
        System.out.println 
        ("Boolean variable: "+a+"\n"+
        "Byte variable: "+b+"\n"+
        "Short variable: "+c+"\n"+
        "Int variable: "+d+"\n"+
        "Long variable: "+e+"\n"+
        "Float variable: "+f+"\n"+
        "Double variable: "+g+"\n"+
        "Char variable: "+h+"\n"+
        "String variable: "+i);

    }
}